button7 = Button(root, text="7", padx=40, pady=20, command=lambda: button_click(7),fg="yellow",bg="black")
button8 = Button(root, text="8", padx=40, pady=20, command=lambda: button_click(8),fg="yellow",bg="black")
button9 = Button(root, text="9", padx=40, pady=20, command=lambda: button_click(9),fg="yellow",bg="black")
button0 = Button(root, text="0", padx=40, pady=20, command=lambda: button_click(0),fg="yellow",bg="black")
buttonadd = Button(root, text="+", padx=40, pady=20, command=lambda: button_click('+'),fg="yellow",bg="black")
buttonsub = Button(root, text="-", padx=40, pady=20, command=lambda: button_click('-'),fg="yellow",bg="black")
buttonmul = Button(root, text="*", padx=40, pady=20, command=lambda: button_click('*'),fg="yellow",bg="black")
buttondiv = Button(root, text="/", padx=40, pady=20, command=lambda: button_click('/'),fg="yellow",bg="black")
buttonequal = Button(root, text="=", padx=40, pady=20, command=calculate,fg="yellow",bg="black")
buttonclear = Button(root, text="C", padx=40, pady=20, command=clear,fg="yellow",bg="black")
button7.grid(row=1, column=0)
