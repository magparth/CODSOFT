from tkinter import *
def button_click(number):
    current = e.get()
    e.delete(0, END)
    e.insert(0, current + str(number))
def clear():
    e.delete(0, END)
def calculate():
    try:
        result = eval(e.get())
        e.delete(0, END)
        e.insert(0, result)
    except:
        e.delete(0, END)
        e.insert(0, "Error")
root = Tk()
root.title("Calculator")
e = Entry(root, width=40, borderwidth=5)
e.grid(row=0, column=0, columnspan=4, padx=10, pady=10)
button1 = Button(root, text="1", padx=40, pady=20, command=lambda: button_click(1),fg="yellow",bg="black")
